using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class TreePosition
{
    private int x;
    private int y;

    public TreePosition( int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }

    public void setX(int x)
    {
        this.x = x;
    }

    public void setY(int y)
    {
        this.y = y;
    }
}